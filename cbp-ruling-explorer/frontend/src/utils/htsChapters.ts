/**
 * 从 HS code 提取前 2 位章节号，返回 "CH{章节} · {名称}" 标签
 * 例：8471.30.0100 → "CH84 · 电机、电气设备"
 */
export function getChapterLabel(hsCodes: string[] | undefined | null): string {
  if (!hsCodes || hsCodes.length === 0) return '';
  const first = hsCodes[0];
  const chapter = first.replace(/\./g, '').slice(0, 2);
  if (!chapter || !/^\d{2}$/.test(chapter)) return '';
  const name = HTS_CHAPTERS[chapter] || '';
  return name ? `CH${chapter} · ${name}` : `CH${chapter}`;
}

/**
 * 从 HS code 提取前 2 位章节号，只返回数字
 */
export function getChapterNum(hsCode: string): string {
  const ch = hsCode.replace(/\./g, '').slice(0, 2);
  return /^\d{2}$/.test(ch) ? ch : '';
}
